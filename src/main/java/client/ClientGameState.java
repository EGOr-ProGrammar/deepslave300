package client;

import shared.WorldSnapshot;

/**
 * Хранилище текущего состояния игры на стороне клиента.
 * <p>
 * Служит общим ресурсом для синхронизации между двумя потоками:
 * 1. Сетевой поток (ClientConnection): постоянно обновляет поле snapshot при получении данных от сервера.
 * 2. Поток рендеринга (ClientMain): читает snapshot для отрисовки кадра на экране.
 */
public class ClientGameState {

    /**
     * Последний полученный снимок игрового мира.
     * <p>
     * Гарантирует, что изменения, записанные сетевым потоком, будут мгновенно видны потоку отрисовки.
     * Если snapshot равен null, значит клиент еще не получил ни одного пакета от сервера.
     */
    public volatile WorldSnapshot snapshot;
}
