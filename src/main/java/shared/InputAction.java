package shared;

/**
 * Перечисление возможных действий игрока.
 * Используется для передачи команд от клиента к серверу.
 */
public enum InputAction {
    // Движение
    MOVE_LEFT,
    MOVE_UP,
    MOVE_RIGHT,
    MOVE_DOWN,

    // Игровые действия
    WAIT,           // Пропуск хода
    USE_STAIRS,     // Использование лестницы вниз/вверх
    PICKUP,         // Подбор предмета
    USE_ITEM,       // Использование предмета из инвентаря
    DROP_ITEM,      // Выбросить предмет

    // Боевые действия
    ATTACK,         // Атака в направлении движения

    // Системные
    QUIT,           // Выход из игры
    MENU,           // Открыть меню
    INVENTORY       // Открыть инвентарь
}
